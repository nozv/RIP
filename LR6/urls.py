from django.contrib import admin
from django.urls import include, path
from Vet import views as vetviews
from rest_framework import routers
from admin_tools import urls

router = routers.DefaultRouter()
router.register(r'services', vetviews.ServiceViewSet)
router.register(r'appointments', vetviews.AppointmentViewSet)
router.register(r'doctors', vetviews.DoctorViewSet)
router.register(r'schedule', vetviews.ScheduleViewSet)

urlpatterns = [
    path('', include(router.urls)),
    path('api-auth/', include('rest_framework.urls', namespace='rest_framework')),
    path('admin/', admin.site.urls),
]